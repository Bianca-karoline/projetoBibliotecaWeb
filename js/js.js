function exibirform(id){
    let display = document.getElementById(id).style.display;
        if(display == "none"){
            document.getElementById(id).style.display = 'block';
        }
        else{
            document.getElementById(id).style.display = 'none';
        }
}
function editar(id){
    let display = document.getElementById(id).style.display;
        if(display == "none"){
            document.getElementById(id).style.display = 'block';
        }
        else{
            document.getElementById(id).style.display = 'none';
        }
}